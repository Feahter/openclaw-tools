def get_current_chapter():
    """获取当前写到的章节"""
    if os.path.exists(STATE_FILE):
        with open(STATE_FILE, 'r') as f:
            state = json.load(f)
            return state.get('current_chapter', 71)
    return 71

def save_current_chapter(chapter):
    """保存当前章节"""
    state = {
        'current_chapter': chapter,
        'last_updated': datetime.now().isoformat()
    }
    os.makedirs('data', exist_ok=True)
    with open(STATE_FILE, 'w') as f:
        json.dump(state, f, indent=2)

def to_chinese_num(n):
    """转换数字到中文"""
    if n <= 10:
        return ['', '一', '二', '三', '四', '五', '六', '七', '八', '九', '十'][n]
    elif n < 20:
        return '十' + to_chinese_num(n - 10)
    elif n < 100:
        return to_chinese_num(n // 10) + '十' + to_chinese_num(n % 10)
    else:
        return str(n)

def chapter_exists(chapter_num):
    """检查章节是否已存在"""
    if not os.path.exists(NOVEL_DIR):
        return False
    
    for f in os.listdir(NOVEL_DIR):
        # 检查是否以"第X章"开头，排除章节摘要
        if '章节摘要' not in f:
            # 同时检查中文和阿拉伯数字格式
            cn = to_chinese_num(chapter_num)
            if f'第{chapter_num}章' in f or f'第{cn}章' in f:
                return True
    
    return False
    return False

