def get_changsheng_state(tian_gan: str, branch: str) -> str:
    """
    获取某天干在某地支的长生状态
    
    算法：(branch_idx - start_idx + 12) % 12 = state_idx
    其中 start_idx 是该天干的长生地支在地支序列中的索引
    
    长生十二宫顺序：长生→沐浴→冠带→临官→帝旺→衰→病→死→墓→绝→胎→养
    
    Args:
        tian_gan: 天干（甲、乙、丙、丁、戊、己、庚、辛、壬、癸）
        branch: 地支（子、丑、寅、卯、辰、巳、午、未、申、酉、戌、亥）
    
    Returns:
        str: 长生状态（长生、沐浴、冠带、临官、帝旺、衰、病、死、墓、绝、胎、养）
    """
    start_zhi = CHANGSHENG_START.get(tian_gan)
    if start_zhi is None:
        return ""
    
    start_idx = ZHI_IDX.get(start_zhi, -1)
    branch_idx = ZHI_IDX.get(branch, -1)
    
    if start_idx < 0 or branch_idx < 0:
        return ""
    
    state_idx = (branch_idx - start_idx + 12) % 12
    return CHANGSHENG_ORDER[state_idx]


def get_changsheng_state_idx(tian_gan: str, branch: str) -> int:
    """
    获取某天干在某地支的长生状态索引（0-11）
    0=长生, 1=沐浴, ... 11=养
    """
    start_zhi = CHANGSHENG_START.get(tian_gan)
    if start_zhi is None:
        return -1
    
    start_idx = ZHI_IDX.get(start_zhi, -1)
    branch_idx = ZHI_IDX.get(branch, -1)
    
    if start_idx < 0 or branch_idx < 0:
        return -1
    
    return (branch_idx - start_idx + 12) % 12


def get_changsheng_by_idx(stem_idx: int, branch_idx: int) -> str:
    """
    通过索引获取长生状态（供 bazi_engine 内部使用）
    
    Args:
        stem_idx: 天干索引(0-9)
        branch_idx: 地支索引(0-11)
    
    Returns:
        str: 长生状态
    """
    table = CHANGSHENG_TABLE.get(stem_idx, [0] * 12)
    state_idx = table[branch_idx % 12]
    return CHANGSHENG_ORDER[state_idx]


def get_all_changsheng_for_stem(tian_gan: str) -> dict:
    """获取某天干在所有地支的长生状态"""
    result = {}
    for zhi, idx in ZHI_IDX.items():
        state = get_changsheng_state(tian_gan, zhi)
        if state:
            result[zhi] = state
    return result


