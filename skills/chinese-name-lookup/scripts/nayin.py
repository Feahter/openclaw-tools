def get_nayin(stem_idx: int, branch_idx: int) -> str:
    """
    获取任意干支的纳音五行

    参数：
        stem_idx: 天干索引（0-9）
        branch_idx: 地支索引（0-11）

    返回：
        纳音名称，如"海中金"、"炉中火"等
    """
    return _NAYIN_60.get((stem_idx, branch_idx), "海中金")


def get_nayin_element(nayin_name: str) -> str:
    """
    获取纳音对应的正五行

    参数：nayin_name 纳音名称

    返回：正五行（木/火/土/金/水）
    """
    return NAYIN_ELEMENT_MAP.get(nayin_name, "土")


def get_nayin_personality(nayin_name: str) -> str:
    """
    获取纳音性格描述

    参数：nayin_name 纳音名称

    返回：性格特点描述
    """
    return NAYIN_PERSONALITY.get(nayin_name, "无特殊描述")


def analyze_nayin_relation(nayin1: str, nayin2: str) -> Dict:
    """
    分析两个纳音之间的关系

    参数：
        nayin1: 第一个纳音（如年柱纳音）
        nayin2: 第二个纳音（如日柱纳音）

    返回：
        {
            "element1": 正五行,
            "element2": 正五行,
            "relation": 关系描述,
            "quality": 好/中/差,
            "detail": 详细分析
        }
    """
    elem1 = get_nayin_element(nayin1)
    elem2 = get_nayin_element(nayin2)

    if elem1 == elem2:
        quality = "中"
        detail = NAYIN_RELATIONS.get((elem1, elem2), "无特殊描述")
    elif (elem1, elem2) in NAYIN_TABOO_PAIRS:
        quality = "差"
        detail = f"{elem1}与{elem2}相克，纳音关系不吉，多波折竞争。"
    elif (elem1, elem2) in [
        ("金", "水"), ("水", "木"), ("木", "火"),
        ("火", "土"), ("土", "金"),  # 我生者
        ("金", "木"), ("木", "土"), ("土", "水"),
        ("水", "火"), ("火", "金"),  # 我克者
    ]:
        quality = "好"
        detail = NAYIN_RELATIONS.get((elem1, elem2),
            NAYIN_RELATIONS.get((elem2, elem1),
                f"{elem1}与{elem2}有生克关系。"))
    else:
        quality = "中"
        detail = f"{elem1}与{elem2}关系一般，需要其他因素配合。"

    return {
        "element1": elem1,
        "element2": elem2,
        "nayin1": nayin1,
        "nayin2": nayin2,
        "relation": detail,
        "quality": quality,
    }


