def get_tiao_hou(tian_gan: str, yue_zhi: str) -> dict:
    """
    查询调候喜忌（穷通宝鉴 V2）
    
    Args:
        tian_gan: 天干（甲、乙、丙、丁、戊、己、庚、辛、壬、癸）
        yue_zhi: 月令地支（寅、卯、辰、巳、午、未、申、酉、戌、亥，子、丑）
    
    Returns:
        dict: {"主神": [...], "辅神": [...], "忌避": [...], "喜用": [...]}
        V2格式含主神/辅神/忌避，同时包含"喜用"(=主神+辅神)兼容旧代码
    """
    key = f"{tian_gan}_{yue_zhi}"
    info = TIAO_HOU_TABLE.get(key, {})
    if info and "喜用" not in info:
        # V2兼容层：生成喜用=主神+辅神
        info = dict(info)
        info["喜用"] = info.get("主神", []) + info.get("辅神", [])
    return info


def get_tiao_hou_xiyong(tian_gan: str, yue_zhi: str) -> list:
    """
    获取喜用神列表（主神+辅神，按优先级排序）
    """
