def get_tiao_hou(tian_gan: str, yue_zhi: str) -> dict:
    """
    查询调候喜忌（穷通宝鉴）
    
    Args:
        tian_gan: 天干（甲、乙、丙、丁、戊、己、庚、辛、壬、癸）
        yue_zhi: 月令地支（寅、卯、辰、巳、午、未、申、酉、戌、亥、子、丑）
    
    Returns:
        dict: {"喜用": [...], "忌避": [...], "原则": str}
        若组合不存在返回空字典
    """
    key = f"{tian_gan}_{yue_zhi}"
    return TIAO_HOU_TABLE.get(key, {})


def get_tiao_hou_xiyong(tian_gan: str, yue_zhi: str) -> list:
    """直接获取喜用神列表"""
    info = get_tiao_hou(tian_gan, yue_zhi)
    return info.get("喜用", [])


def get_tiao_hou_jibi(tian_gan: str, yue_zhi: str) -> list:
    """直接获取忌避神列表"""
    info = get_tiao_hou(tian_gan, yue_zhi)
    return info.get("忌避", [])


def get_tiao_hou_principle(tian_gan: str, yue_zhi: str) -> str:
    """直接获取调候原则"""
    info = get_tiao_hou(tian_gan, yue_zhi)
    return info.get("原则", "")


def all_tiao_hou_for_stem(tian_gan: str) -> dict:
    """获取某天干全年十二月的调候喜忌"""
    result = {}
    for yz in YUE_ZHI:
        info = get_tiao_hou(tian_gan, yz)
        if info:
            result[yz] = info
    return result


