def get_char_radical(char: str) -> str:
    """获取汉字的偏旁"""
    # 1. 精确查表
    if char in CHAR_RADICAL:
        return CHAR_RADICAL[char]
    # 2. 未知，返回字符本身
    return char


def get_zodiac_preferences(zodiac: str) -> dict:
    """获取生肖宜忌信息"""
    return ZODIAC_PREFERENCES.get(zodiac, {})


def score_char_for_zodiac(char: str, zodiac: str) -> int:
    """
    评估单个汉字对该生肖的吉凶评分
    返回: 正分（宜用）、负分（忌用）、0（中性）
    """
    prefs = get_zodiac_preferences(zodiac)
    if not prefs:
        return 0

    lucky = set(prefs.get("lucky_chars", []))
    avoid = set(prefs.get("avoid_chars", []))

    # 1. 精确匹配偏旁名
    if char in lucky:
        return 10
    if char in avoid:
        return -10

    # 2. 获取偏旁，查偏旁表
    radical = get_char_radical(char)
    if radical in lucky:
        return 8  # 字含宜用偏旁
    if radical in avoid:
        return -8  # 字含忌用偏旁

    # 3. 五行元素偏旁（较低权重）
    elem = RADICAL_TO_ELEMENT.get(radical)
    if elem:
        if elem in lucky:
            return 5
        if elem in avoid:
            return -5

    return 0


def score_name_for_zodiac(name_chars: str, zodiac: str) -> dict:
    """
    评估名字对该生肖的吉凶
    返回详细评分
    """
    prefs = get_zodiac_preferences(zodiac)
    if not prefs:
        return {"score": 0, "details": [], "summary": "未知生肖"}

    total_score = 0
    details = []

    for char in name_chars:
        char_score = score_char_for_zodiac(char, zodiac)
        total_score += char_score

        if char_score > 0:
            radical = get_char_radical(char)
            details.append(f"「{char}」(含{radical}，宜)")
        elif char_score < 0:
            radical = get_char_radical(char)
            details.append(f"「{char}」(含{radical}，忌)")

    # 归一化到0-30
    normalized = int((total_score + 20) * 30 / 40)
    normalized = max(0, min(30, normalized))

    lucky_nums = prefs.get("lucky_numbers", [])
    directions = prefs.get("directions", [])

    if total_score > 5:
        summary = f"名字「{name_chars}」含宜用偏旁，大吉"
    elif total_score > 0:
        summary = f"名字「{name_chars}」含宜用偏旁"
    elif total_score < -5:
        summary = f"名字「{name_chars}」含忌用偏旁，凶"
    elif total_score < 0:
        summary = f"名字「{name_chars}」含忌用偏旁"
    else:
        summary = f"名字「{name_chars}」偏旁中性"

    return {
        "score": normalized,
        "raw_score": total_score,
        "details": details[:5],
        "summary": summary,
        "lucky_numbers": lucky_nums,
        "directions": directions,
    }


